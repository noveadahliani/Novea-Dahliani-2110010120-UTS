    private void btnExportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportActionPerformed
         // Eksport File
        try{
            BufferedWriter out = new BufferedWriter (new FileWriter("DataBarang.ext"));
            
            for(int i = 0; i < tblBarang.getRowCount(); i++){ // rows
                for (int j = 0; j < tblBarang.getColumnCount(); j++){ // columns
                    out.write(tblBarang.getValueAt(i, j).toString()+" ");
                }
                out.newLine();
            }            
            JOptionPane.showMessageDialog(null, "Berhasil Diexport dalam File");
            
            out.close();
        }catch (Exception e){
            System.err.println("Error: "+e.getMessage());
        }
    }//GEN-LAST:event_btnExportActionPerformed

    private void btnImportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImportActionPerformed
       // Import File     
        String filePath = "DataBarang.ext";
        File file = new File(filePath);
        try{
            FileReader fr = new FileReader(file);
            BufferedReader br = new BufferedReader(fr);
            
        DefaultTableModel model = (DefaultTableModel)tblBarang.getModel();
        Object[] lines = br.lines().toArray();
        
        for(int i = 0; i < lines.length; i++ ){
            String[] row = lines[i].toString().split(" ");
            model.addRow(row);
        }            
        JOptionPane.showMessageDialog(null, "Berhasil mengimport data");
            
        }catch (Exception e){
            System.err.println("Error: "+e.getMessage());
        }
    }//GEN-LAST:event_btnImportActionPerformed

    private void btnLaporanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLaporanActionPerformed
        //Header Laporan
        TextAreaReport.setText(TextAreaReport.getText() + "====================================================\n");
        TextAreaReport.setText(TextAreaReport.getText() + "                            LAPORAN                \n");
        TextAreaReport.setFont(TextAreaReport.getFont().deriveFont(Font.BOLD, 14f));
        TextAreaReport.setText(TextAreaReport.getText() + "====================================================\n");
        // Laporan
        DefaultTableModel model = (DefaultTableModel) tblBarang.getModel();
        int nRow = model.getRowCount(), nCol = model.getColumnCount();
        Object[][] tableData = new Object[nRow][nCol];
            for (int i = 0; i < nRow; i++) {
                for (int j = 0; j < nCol; j++) {
                tableData[i][j] = model.getValueAt(i, j);
            TextAreaReport.append(String.valueOf(tableData[i][j]) + " ");
            }
        TextAreaReport.append("\n");
      }
    }//GEN-LAST:event_btnLaporanActionPerformed

    private void tblBarangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBarangMouseClicked
        //Menampilkan Data dari Jtable ke TextFiled
        int selectedRow = tblBarang.getSelectedRow();
        
        DefaultTableModel model = (DefaultTableModel) tblBarang.getModel();
        tfId.setText(model.getValueAt(selectedRow, 0).toString());
        tfNama.setText(model.getValueAt(selectedRow, 1).toString());
        tfJumlah.setText(model.getValueAt(selectedRow, 3).toString());
        String comboSub = model.getValueAt(selectedRow, 2).toString();

        for (int i = 0; i < cmbJenis.getItemCount(); i++) {
            if (cmbJenis.getItemAt(i).toString().equalsIgnoreCase(comboSub)) {
        cmbJenis.setSelectedIndex(i);
        break;
         }
    }//GEN-LAST:event_tblBarangMouseClicked
        tfHarga.setText(model.getValueAt(selectedRow, 4).toString());

    }       
